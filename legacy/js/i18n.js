// Current language (default: Arabic)
let currentLang = localStorage.getItem('portal-lang') || 'ar';

/**
 * Get translated text for a key
 * @param {string} key - Translation key
 * @returns {string} - Translated text
 */
function t(key) {
  return translations[currentLang]?.[key] || translations['en']?.[key] || key;
}

/**
 * Set the current language and update the page
 * @param {string} lang - Language code ('ar' or 'en')
 */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('portal-lang', lang);

  // Update document direction
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;

  // Update all translatable elements
  updateTranslations();

  // Update language toggle buttons
  updateLanguageToggle();

  // Sync with Next.js cookie
  document.cookie = `language=${lang}; path=/; max-age=31536000`; // 1 year

  // Update Report Link (Specific to Conversational Program Report)
  const reportLink = document.getElementById('full-schedule-btn');
  if (reportLink) {
    if (lang === 'ar') {
      reportLink.href = 'full-schedule-ar.html';
    } else {
      reportLink.href = 'full-schedule.html';
    }
  }
}

/**
 * Update all elements with data-i18n attribute
 */
function updateTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = t(key);
    // Check if translation contains HTML tags
    if (translation.includes('<') && translation.includes('>')) {
      element.innerHTML = translation;
    } else {
      element.textContent = translation;
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    element.placeholder = t(key);
  });

  // Update title attributes
  document.querySelectorAll('[data-i18n-title]').forEach(element => {
    const key = element.getAttribute('data-i18n-title');
    element.title = t(key);
  });
}

/**
 * Update language toggle button states
 */
function updateLanguageToggle() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const btnLang = btn.getAttribute('data-lang');
    btn.classList.toggle('active', btnLang === currentLang);
  });
}

/**
 * Toggle between Arabic and English
 */
function toggleLanguage() {
  setLanguage(currentLang === 'ar' ? 'en' : 'ar');
}

/**
 * Initialize i18n on page load
 */
function initI18n() {
  // Set initial language
  setLanguage(currentLang);

  // Add click handlers to language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initI18n);
